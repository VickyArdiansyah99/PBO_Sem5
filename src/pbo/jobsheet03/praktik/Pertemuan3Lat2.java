package pbo.jobsheet03.praktik;

public class Pertemuan3Lat2 {

    public static void main(String[] args) {

        int a = 20;
        System.out.println(a+=5); //artinya sama dengan 20+5 = 25

        int b = 10;
        System.out.println(b-=5); //artinya sama dengan 10-5 = 5

        int c = 5;
        System.out.println(c*=5); //artinya sama dengan 5*+5 = 25

        float d = 10;
        System.out.println(d/=3); //artinya sama dengan 10/3 = 3.333

        int e = 10;
        System.out.println(e%=3); //artinya sama dengan 10 % 3 = 1

    }

}
